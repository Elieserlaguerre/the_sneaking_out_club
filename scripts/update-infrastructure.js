import fs from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";

/**
 * Serialize JS objects while preserving raw identifiers
 * for icons and process.env values, and remove quotes from keys.
 */
function stringifyInfrastructure(data, indent = 2) {
	const rawKeys = ["icon", "href"];

	function replacer(key, value) {
		if (rawKeys.includes(key)) {
			// process.env.* should remain raw JS code
			if (typeof value === "string" && value.startsWith("process.env.")) {
				return `__RAW__${value}`;
			}
			// icon names (capitalized) should also be raw JS identifiers
			if (key === "icon" && /^[A-Z]/.test(value)) {
				return `__RAW__${value}`;
			}
		}
		return value;
	}

	// Serialize with placeholder markers
	let json = JSON.stringify(data, replacer, indent);

	// Cleanly replace raw markers with unquoted code (no curly braces)
	json = json.replace(/"__RAW__(process\.env\.[^"]+)"/g, "$1");
	json = json.replace(/"__RAW__([A-Z][A-Za-z0-9_]*)"/g, "$1");

	// 🧩 Remove quotes around object keys (but keep string values)
	// Matches patterns like: "key": or "key" :
	json = json.replace(/"([^"]+)"\s*:/g, "$1:");

	return json;
}

/**
 * Deep comparison helper
 */
function compareInfrastructure(oldData, newData) {
	const changes = [];

	const oldSections = new Map(oldData.map((s) => [s.sectionTitle, s]));
	const newSections = new Map(newData.map((s) => [s.sectionTitle, s]));

	for (const [sectionTitle, newSection] of newSections.entries()) {
		const oldSection = oldSections.get(sectionTitle);

		if (!oldSection) {
			changes.push(`🟩 Added section: "${sectionTitle}"`);
			continue;
		}

		if (oldSection.display !== newSection.display) {
			changes.push(`🟨 Toggled display for section: "${sectionTitle}" (${oldSection.display} → ${newSection.display})`);
		}

		const oldItems = new Map(oldSection.list.map((i) => [i.name, i]));
		const newItems = new Map(newSection.list.map((i) => [i.name, i]));

		for (const [name, newItem] of newItems.entries()) {
			const oldItem = oldItems.get(name);
			if (!oldItem) {
				changes.push(`🟩 Added item "${name}" in section "${sectionTitle}"`);
				continue;
			}

			for (const key of Object.keys(newItem)) {
				if (JSON.stringify(oldItem[key]) !== JSON.stringify(newItem[key])) {
					changes.push(`🟨 Updated "${key}" for item "${name}" in section "${sectionTitle}" (${oldItem[key]} → ${newItem[key]})`);
				}
			}
		}

		for (const [name] of oldItems.entries()) {
			if (!newItems.has(name)) {
				changes.push(`🟥 Removed item "${name}" from section "${sectionTitle}"`);
			}
		}
	}

	for (const [sectionTitle] of oldSections.entries()) {
		if (!newSections.has(sectionTitle)) {
			changes.push(`🟥 Removed section: "${sectionTitle}"`);
		}
	}

	return changes;
}

/**
 * Updates the platform-infrastructure.js file if the provided data differs from the existing data.
 */
export async function updateInfrastructure(infrastructureData) {
	try {
		const __filename = fileURLToPath(import.meta.url);
		const __dirname = path.dirname(__filename);
		const infraFilePath = path.resolve(__dirname, "./platform-infrastructure.js");

		if (!Array.isArray(infrastructureData)) {
			throw new Error("Input must be an array of infrastructure objects.");
		}

		let existingData = [];
		try {
			const fileContent = await fs.readFile(infraFilePath, "utf8");
			const match = fileContent.match(/export\s+const\s+PLATFORM_INFRASTRUCTURE\s*=\s*(\[[\s\S]*?\]);/);
			if (match && match[1]) {
				existingData = JSON.parse(match[1]);
			}
		} catch {
			console.warn("⚠️ No existing platform-infrastructure.js found. A new one will be created.");
		}

		const isEqual = JSON.stringify(existingData, null, 2) === JSON.stringify(infrastructureData, null, 2);

		if (isEqual) {
			console.log("✅ platform-infrastructure.js is already up to date.");
			return { updated: false, changes: [] };
		}

		const changes = compareInfrastructure(existingData, infrastructureData);

		if (changes.length > 0) {
			console.log("🧩 Detected infrastructure changes:");
			for (const change of changes) console.log(`   ${change}`);
		}

		const importBlock = `import { AcademicCapIcon, UsersIcon, BriefcaseIcon, BuildingOffice2Icon, CurrencyDollarIcon, GlobeEuropeAfricaIcon, HomeModernIcon, QuestionMarkCircleIcon, ShoppingCartIcon, UserGroupIcon, InformationCircleIcon, DocumentIcon, UserIcon } from "@heroicons/react/24/outline";
import { MdOutlineGppGood } from "react-icons/md";
import { FaRegHandshake } from "react-icons/fa";
import { BiSupport } from "react-icons/bi";
import { RiStockFill } from "react-icons/ri";
\n`;

		const updatedContent = `/*
 * AUTO-GENERATED FILE: platform-infrastructure.js
 * Generated by scripts/update-infrastructure.js
 * DO NOT EDIT THIS FILE DIRECTLY — changes will be overwritten.
 */

${importBlock}

export const PLATFORM_INFRASTRUCTURE = ${stringifyInfrastructure(infrastructureData, 2)};

`;

		await fs.writeFile(infraFilePath, updatedContent, "utf8");
		console.log("🛠️ platform-infrastructure.js has been successfully updated.");

		return { updated: true, changes };
	} catch (error) {
		console.error("❌ Failed to update infrastructure file:", error);
		throw error;
	}
}
